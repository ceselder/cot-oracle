"""
Task 7: CoT Summary Prediction (~15K examples)

Given activations at fixed 25-token stride positions, predict a concise faithful summary
of the chain of thought. Summaries are pre-generated by Gemini 2.5 Flash Lite.

This teaches the oracle to read activations and produce a semantic description
of what the model actually computed â€” the foundation for unfaithfulness detection.
"""

import json
import random

from transformers import AutoTokenizer


def load_cot_summary_data(
    corpus_path: str,
    summaries_path: str,
    tokenizer: AutoTokenizer,
    model_name: str,
    layer_percents: list[int],
    num_examples: int = 15000,
    max_sentences: int = 15,
    seed: int = 42,
) -> list[dict]:
    """
    Generate CoT summary prediction training data.

    Each example: activations at strided CoT positions -> faithful summary text.
    Summaries come from summaries.jsonl (LLM-generated).
    """
    from cot_utils import layer_percent_to_layer, get_cot_stride_positions

    random.seed(seed)

    # Load corpus
    corpus = {}
    with open(corpus_path) as f:
        for line in f:
            if line.strip():
                entry = json.loads(line)
                corpus[entry["id"]] = entry

    # Load summaries
    summaries = {}
    with open(summaries_path) as f:
        for line in f:
            if line.strip():
                s = json.loads(line)
                if not s["summary"].startswith("[FAILED"):
                    summaries[s["id"]] = s["summary"]

    # Match corpus entries with summaries
    matched = []
    for eid, entry in corpus.items():
        if eid in summaries:
            matched.append((entry, summaries[eid]))

    print(f"  Matched corpus+summary entries: {len(matched)}")

    if not matched:
        raise ValueError("No matched entries found. Check summaries_path.")

    layers = [layer_percent_to_layer(model_name, lp) for lp in layer_percents]

    datapoints = []

    while len(datapoints) < num_examples:
        entry, summary = random.choice(matched)

        messages = [{"role": "user", "content": entry["question"]}]
        formatted = tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=True,
            enable_thinking=True,
        )
        cot_text = entry["cot_response"]
        think_end = cot_text.find("</think>")
        if think_end != -1:
            cot_text = cot_text[:think_end]
        full_text = formatted + cot_text
        context_ids = tokenizer(full_text, add_special_tokens=False)["input_ids"]
        prompt_len = len(tokenizer(formatted, add_special_tokens=False)["input_ids"])

        positions = get_cot_stride_positions(
            prompt_token_count=prompt_len,
            total_token_count=len(context_ids),
            stride=25,
            max_positions=max_sentences,
        )
        if len(positions) < 2:
            continue

        N = len(positions)
        context_slice = context_ids[:positions[-1] + 1]

        prompt = (
            f"Activations from {N} strided CoT positions (every 25 tokens). "
            f"Generate a concise summary of the thought process."
        )

        datapoints.append({
            "datapoint_type": "cot_summary",
            "prompt": prompt,
            "target_response": summary,
            "layers": layers,  # Multi-layer: [L25%, L50%, L75%]
            "num_positions": N,
            "context_input_ids": context_slice,
            "context_positions": list(positions),
        })

    return datapoints[:num_examples]
